import numpy as np

def diesel_cycle(T1, r, rc, gamma):

    # ------------------------------------------------
    # GAS CONSTANTS
    # ------------------------------------------------

    R = 287  # J/kg-K

    cv = R / (gamma - 1)

    cp = gamma * cv

    # ------------------------------------------------
    # STATE 1 → 2
    # Isentropic Compression
    # ------------------------------------------------

    T2 = T1 * (r ** (gamma - 1))

    # ------------------------------------------------
    # STATE 2 → 3
    # Constant Pressure Heat Addition
    # ------------------------------------------------

    T3 = T2 * rc

    # ------------------------------------------------
    # STATE 3 → 4
    # Isentropic Expansion
    # ------------------------------------------------

    T4 = T3 * ((rc / r) ** (gamma - 1))

    # ------------------------------------------------
    # ENTROPY VALUES
    # ------------------------------------------------

    S1 = 100

    # Isentropic compression

    S2 = S1

    # Constant pressure heat addition

    S3 = S2 + cp * np.log(T3 / T2)

    # Isentropic expansion

    S4 = S3

    # ------------------------------------------------
    # HEAT TRANSFER
    # ------------------------------------------------

    Q_in = cp * (T3 - T2)

    Q_out = cv * (T4 - T1)

    # ------------------------------------------------
    # NET WORK
    # ------------------------------------------------

    W_net = Q_in - Q_out

    # ------------------------------------------------
    # EFFICIENCY
    # ------------------------------------------------

    efficiency = (W_net / Q_in) * 100

    # ------------------------------------------------
    # RETURN RESULTS
    # ------------------------------------------------

    return {

        "T": [T1, T2, T3, T4],

        "S": [S1, S2, S3, S4],

        "Q_in": Q_in,

        "Q_out": Q_out,

        "W_net": W_net,

        "efficiency": efficiency
    }