import numpy as np

def otto_cycle(T1, r, T3, gamma):

    # ------------------------------------------------
    # GAS CONSTANTS
    # ------------------------------------------------

    R = 287   # J/kg-K

    cv = R / (gamma - 1)

    # ------------------------------------------------
    # REFERENCE CONDITIONS
    # ------------------------------------------------
    # Assumed normalized initial conditions
    # for thermodynamic visualization

    V1 = 0.001      # m³

    P1 = 101325     # Pa

    S1 = 100        # J/kg-K reference entropy

    # ------------------------------------------------
    # STATE 1 → 2
    # Isentropic Compression
    # ------------------------------------------------

    T2 = T1 * (r ** (gamma - 1))

    V2 = V1 / r

    P2 = P1 * (r ** gamma)

    S2 = S1

    # ------------------------------------------------
    # STATE 2 → 3
    # Constant Volume Heat Addition
    # ------------------------------------------------

    V3 = V2

    P3 = P2 * (T3 / T2)

    S3 = S2 + cv * np.log(T3 / T2)

    # ------------------------------------------------
    # STATE 3 → 4
    # Isentropic Expansion
    # ------------------------------------------------

    T4 = T3 / (r ** (gamma - 1))

    V4 = V1

    P4 = P3 * (V3 / V4) ** gamma

    S4 = S3

    # ------------------------------------------------
    # HEAT TRANSFER
    # ------------------------------------------------

    Q_in = cv * (T3 - T2)

    Q_out = cv * (T4 - T1)

    # ------------------------------------------------
    # WORK
    # ------------------------------------------------

    W_net = Q_in - Q_out

    # ------------------------------------------------
    # EFFICIENCY
    # ------------------------------------------------

    efficiency = (W_net / Q_in) * 100

    # ------------------------------------------------
    # RETURN RESULTS
    # ------------------------------------------------

    return {

        "T": [T1, T2, T3, T4],

        "P": [P1, P2, P3, P4],

        "V": [V1, V2, V3, V4],

        "S": [S1, S2, S3, S4],

        "Q_in": Q_in,

        "Q_out": Q_out,

        "W_net": W_net,

        "efficiency": efficiency,

        "gamma": gamma,

        "R": R,

        "cv": cv
    }