import numpy as np

def otto_cycle(T1, r, T3, gamma):

    # ------------------------------------------------
    # CONSTANTS
    # ------------------------------------------------

    R = 287

    cv = R / (gamma - 1)

    # ------------------------------------------------
    # STATE 1 → 2
    # Isentropic Compression
    # ------------------------------------------------

    T2 = T1 * (r ** (gamma - 1))

    # ------------------------------------------------
    # STATE 2 → 3
    # Constant Volume Heat Addition
    # ------------------------------------------------

    # T3 given directly by user

    # ------------------------------------------------
    # STATE 3 → 4
    # Isentropic Expansion
    # ------------------------------------------------

    T4 = T3 / (r ** (gamma - 1))

    # ------------------------------------------------
    # ENTROPY VALUES
    # ------------------------------------------------

    # Reference entropy baseline

    S1 = 0

    # Isentropic compression

    S2 = S1

    # Constant volume heat addition

    S3 = S2 + cv * np.log(T3 / T2)

    # Isentropic expansion

    S4 = S3

    # ------------------------------------------------
    # HEAT TRANSFER
    # ------------------------------------------------

    Q_in = cv * (T3 - T2)

    Q_out = cv * (T4 - T1)

    # ------------------------------------------------
    # WORK
    # ------------------------------------------------

    W_net = Q_in - Q_out

    # ------------------------------------------------
    # EFFICIENCY
    # ------------------------------------------------

    efficiency = (W_net / Q_in) * 100

    # ------------------------------------------------
    # RETURN RESULTS
    # ------------------------------------------------

    return {

        "T": [T1, T2, T3, T4],

        "S": [S1, S2, S3, S4],

        "Q_in": Q_in,

        "Q_out": Q_out,

        "W_net": W_net,

        "efficiency": efficiency
    }